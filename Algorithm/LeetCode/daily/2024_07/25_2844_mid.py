class Solution:
    def minimumOperations(self, num: str) -> int:
        n = len(num)

        def f(tail):
            i = num.rfind(tail[1])
            if i <= 0:
                return n
            i = num.rfind(tail[0], 0, i)
            return n if i < 0 else n - i - 2

        return min(n - ('0' in num), f("00"), f("25"), f("50"), f("75"))
